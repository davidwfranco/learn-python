def add(n1, n2):
    """ Add the 2 given values """
    return n1 + n2

def subtract(n1, n2):
    """ Subtract the 2nd value given from the 1st """
    return n1 - n2

def multiply(n1, n2):
    """ Multiply the 2 given values """
    return n1 * n2

def divide(n1, n2):
    """ Divide the 1st value by the 2nd """
    return n1 / n2